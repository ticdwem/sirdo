        <div class="row">
          <div class="col-lg-12">
            <p class="text-justify">El SIRDO es una Patente Mexicana que permite reciclar los desechos líquidos y sólidos generados por la vivienda y su equipamiento básico. </p>
            <p class="text-justify">Retoma un principio etnobiológico Mexica que consiste en emplear lodos sanitarios de ciertas características físico-químicas como inóculo para la descomposición de los desechos orgánicos sólidos y tiene modalidades de tratamiento en húmedo (con redes colectoras de aguas negras y jabonosas, ya sea separadas o mezcladas) y de tratamiento en seco (como sanitario ecológico). </p>
            <p class="text-justify">Para ambas modalidades existen modelos unifamiliares o colectivos. Los productos que genera son: aguas para acuacultura, agua para riego de cualquier cultivo y biofertilizante (BF) de excelente calidad y libre de patógenos. </p>
            <p class="text-justify">En términos generales podemos  afirmar que el SIRDO recicla el 70% del agua potable de dotación y produce de 25 a 30 Kg/hab/cosecha de BF.</p>
          </div>
        </div>
        <div class="row">
          <div class="col-lg-6"><hr>
            <p class="text-justify">El proceso SIRDO es adaptable a diferentes circunstancias rurales y urbanas, siendo  la modalidad más compleja la del sistema integral de reciclamiento de desechos sólidos para aguas negras y jabonosas</p>
            <p class="text-justify">El desarrollo tecnológico del SIRDO ha generado 5 modalidades o sistemas de acuerdo al tipo de desecho que procesa y a los productos que crea: 2 son sistemas húmedos y 3 son secos. </p>
            <p class="text-justify">La primera Unidad SECA fue construida en Cancún, Estado de Quintana Roo, México, en 1979 y la primera Unidad HUMEDA fue instalada en Mérida, Yucatán, un año después. Aquí se formó la Primera Cooperativa productora de Biofertilizante a partir de desechos orgánicos llamada Muchuc-Baex (“Unámonos” en Maya).</p>
