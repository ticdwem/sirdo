        <h5 class="text-center">LOS OBJETIVOS DE LA TECNOLOGIA SIRDO SON:</h5>
        <p class="text-justify">
          <ul>
            <li>Eliminar la contaminación generada por la materia fecal humana y los desechos orgánicos sólidos.</li>
            <li>Generar soluciones "in situ" al problema del drenaje, evitando así la Contaminación de ríos, etc.</li>
            <li>Ahorro del gasto público en transporte de basura a tiraderos, reduciendo estos focos de contaminación.</li>
            <li>Obtener un biofertilizante (BF) de alta calidad, del reciclaje de materia fecal y orgánica.</li>
          </ul>
        </p>
        <p class="text-justify">Las características de los lodos generados en las Chinampas de Xochimilco fueron investigados en términos de la relación carbón/nitrógeno, pH, oxígeno disuelto, etc. El reto para GTA era generar lodos de características semejantes, acelerar el proceso de descomposición aeróbica, producir un biofertilizante con las características deseables, y llevar todo esto a cabo sin el empleo de aditamentos mecánicos (aireadores, mezcladoras, etc.) lo cual haría que el sistema no fuese económicamente accesible a la población empobrecida a quién se enfoca prioritariamente. </p>
        <p class="text-justify">GTA ha instalado, monitoreado y desarrollado la tecnología SIRDO en la mayor parte de los Estados de la República. Cientos de unidades están operando exitosamente en diversos ambientes ecológicos en todo el país, lo que ha logrado considerables impactos a nivel ingresos y salud pública  que han sido ampliamente documentados en diferentes investigaciones, tales como la financiada por la <a href="http://idl-bnc.idrc.ca/dspace/browse?type=author&order=ASC&rpp=20&value=Grupo+de+Tecnolog%C3%ADa+Alternativa+Sociedad+Civil">International Development Research Centre (IDRC) Canadiense (1989-92). </a></p>
