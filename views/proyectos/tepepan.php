      <h3><strong>TEPEPAN, México, D.F 1983</strong></h3>
</div>
   
    <p class="text-justify">
GTA diseñó y construyó una unidad SIRDO HÚMEDO en un Condominio llamado Los Duraznos en el sur de la Ciudad de México, para 20 familias. Reciclar desechos líquidos y sólidos era una de las metas iniciales, al igual que reducir el uso de agua potable sin sacrificar los bien cuidados y exuberantes jardines. El SIRDO fue integrado al diseño desde su inicio. Esto se tradujo en la instalación de 2 redes paralelas, una para aguas negras y otra para aguas jabonosas.</p>
<p class="text-justify">Una vez instalada y operando la unidad, GTA recibió una beca de la International Development Research Centre (IDRC) para un proyecto de investigación aplicada sobre la viabilidad del reciclaje de agua y desechos.</p>
<p class="text-justify">Esto implicó, hacer cambios dentro del sistema existente, para mejorar la calidad del afluente del subsistema para reciclar las aguas jabonosas, y la calidad del biofertilizante. Uno de los resultados más importantes fué el desarrollo de un complejo sistema de filtros para que el agua tratada pudiera usarse para cría de peces. Otro logro fue identificar las proporciones de BF necesario para optimizar el rendimiento agrícola de los cultivos abonados.</p>
<p class="text-justify"> Los beneficios de la venta del BF fueron invertidos por el Condominio en la instalación de servicios comunales. El BF producido se empleó para cultivar flores en gran escala, triplicando la producción esperada.</p>
<p class="text-justify">El proyecto de Los Duraznos en Tepepan fue tan exitoso que se convirtió en un modelo para otros condominios quienes también instalaron un sistema SIRDO para reciclar su agua y sus desechos. La comunidad de Los Duraznos ha recibido varios premios y su ejemplo fue presentado como una HISTORIA EXITOSA en la Conferencia Internacional convocada por Naciones Unidas, “Socios en la Vida”, en Miami, 1991. El proyecto ha sido ampliamente reportado en los medios masivos de comunicación, debido a la visión de quienes lo hicieron una realidad.</p>
